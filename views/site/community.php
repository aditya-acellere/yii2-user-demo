<h2>Getting support</h2>

<p>Having trouble getting Yii2-user up and running? Something is not working the
way you think it should? Have a question how to implement something with Yii2-user?
Here are some things you might wanna do: </p>

<ul>
    <li>Make sure that problem you got is not described in <a href="https://github.com/dektrium/yii2-user/blob/master/docs/README.md" target="_blank">documentation</a></li>
    <li>Ask a question in <a href="https://gitter.im/dektrium/yii2-user" target="_blank">Gitter chat</a>
    or in <a href="https://groups.google.com/forum/#!forum/yii2-user" target="_blank">Google Group</a></li>
</ul>

<div class="alert alert-warning">
    Please! Don't use Github issue tracker to ask questions, it can only be used
    to report bugs and requesting features.
</div>


<h2>Reporting bugs and issues</h2>

<p>Found a problem or a weird behavior with Yii2-user? Feel free to submit an
issue on the GitHub project. But please keep the following in mind: </p>

<ul>
    <li>Please search to see if your bug was already reported.</li>
    <li>Before opening any issue, please read the generic issue <a href="https://github.com/dektrium/yii2-user/blob/master/CONTRIBUTING.md">guidelines</a>.</li>
    <li>After doing everything above, feel free to submit an issue.</li>
</ul>

